import UIKit

extension UIColor{
    
    static var customGrey: UIColor{
        return  UIColor(named: "customGrey") ?? UIColor.clear
    }
    
    static var customBlack: UIColor{
        return  UIColor(named: "customBlack") ?? UIColor.clear
    }
    
    static var customPurple: UIColor{
        return  UIColor(named: "customPurple") ?? UIColor.clear
    }
}
